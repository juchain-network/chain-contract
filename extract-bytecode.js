#!/usr/bin/env node

/**
 * Extract bytecode from compiled contracts and write to consensus bytecode directory
 * 
 * Usage:
 *   node extract-bytecode.js
 *   npm run extract-bytecode
 */

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

// Contract mapping: contract name -> output filename
const CONTRACTS = {
  'Validators': 'Validators.hex',
  'Proposal': 'Proposal.hex',
  'Punish': 'Punish.hex',
  'Staking': 'Staking.hex',
};

// Paths
const CONTRACT_DIR = __dirname;
const OUT_DIR = path.join(CONTRACT_DIR, 'out');
const BYTECODE_GO_FILE = path.join(CONTRACT_DIR, '..', 'chain.gitlab', 'consensus', 'congress', 'bytecode.go');

/**
 * Extract bytecode from compiled contract JSON
 */
function extractBytecode(contractName) {
  const jsonPath = path.join(OUT_DIR, `${contractName}.sol`, `${contractName}.json`);
  
  if (!fs.existsSync(jsonPath)) {
    throw new Error(`Contract JSON not found: ${jsonPath}`);
  }

  const json = JSON.parse(fs.readFileSync(jsonPath, 'utf8'));
  
  // Try deployedBytecode first (runtime bytecode), fallback to bytecode (creation bytecode)
  let bytecode = json.deployedBytecode?.object || json.bytecode?.object;
  
  if (!bytecode) {
    // Fallback to direct fields
    bytecode = json.deployedBytecode || json.bytecode;
  }

  if (!bytecode || typeof bytecode !== 'string') {
    throw new Error(`Invalid bytecode format for ${contractName}`);
  }

  // Remove 0x prefix if present
  if (bytecode.startsWith('0x')) {
    bytecode = bytecode.substring(2);
  }

  return bytecode;
}

/**
 * Format hex string for Go string literal
 * Splits long hex strings into multiple lines for readability
 */
function formatHexString(hexStr, maxLineLength = 80) {
  // Split into chunks that fit within maxLineLength
  const chunks = [];
  for (let i = 0; i < hexStr.length; i += maxLineLength) {
    chunks.push(hexStr.substr(i, maxLineLength));
  }
  return chunks;
}

/**
 * Generate bytecode.go file with all contract bytecodes
 */
function generateBytecodeGo(contracts) {
  const gitVersion = getGitVersion();
  const timestamp = getCurrentTimestamp();
  
  const lines = [
    'package congress',
    '',
    '// POSA contract bytecodes',
    '// These are auto-generated by extract-bytecode.js script',
    '// DO NOT EDIT MANUALLY - use "npm run build-and-extract" to update',
    `// Git Version: ${gitVersion}`,
    `// Compile Time: ${timestamp}`,
    '',
    'var (',
  ];

  // Add each contract bytecode
  for (const [contractName, bytecode] of Object.entries(contracts)) {
    const varName = `posa${contractName}BytecodeHex`;
    const byteCount = bytecode.length / 2;
    const hexChunks = formatHexString(bytecode, 80);
    
    lines.push(`\t// ${contractName} contract bytecode (${byteCount} bytes)`);
    lines.push(`\t${varName} = `);
    
    // Format as multi-line string literal
    if (hexChunks.length === 1) {
      // Single line
      lines.push(`\t\t"${hexChunks[0]}"`);
    } else {
      // Multi-line using raw string or concatenation
      lines.push(`\t\t"${hexChunks[0]}" +`);
      for (let i = 1; i < hexChunks.length - 1; i++) {
        lines.push(`\t\t"${hexChunks[i]}" +`);
      }
      lines.push(`\t\t"${hexChunks[hexChunks.length - 1]}"`);
    }
    lines.push('');
  }

  lines.push(')');

  return lines.join('\n');
}

/**
 * Get git version (commit hash)
 */
function getGitVersion() {
  try {
    const gitHash = execSync('git rev-parse HEAD', { 
      cwd: CONTRACT_DIR,
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore']
    }).trim();
    return gitHash;
  } catch (error) {
    console.warn('⚠️  Warning: Failed to get git version:', error.message);
    return 'unknown';
  }
}

/**
 * Get current timestamp
 */
function getCurrentTimestamp() {
  return new Date().toISOString();
}

/**
 * Main function
 */
function main() {
  console.log('🔨 Extracting bytecode from compiled contracts...\n');

  // Check if out directory exists
  if (!fs.existsSync(OUT_DIR)) {
    console.error('❌ Error: out/ directory not found. Please run "forge build" first.');
    process.exit(1);
  }

  const contracts = {};
  let successCount = 0;
  let errorCount = 0;

  // Extract bytecode from each contract
  for (const contractName of Object.keys(CONTRACTS)) {
    try {
      console.log(`Extracting ${contractName}...`);
      const bytecode = extractBytecode(contractName);
      contracts[contractName] = bytecode;
      successCount++;
      console.log(`  ✓ ${contractName} (${bytecode.length / 2} bytes)`);
    } catch (error) {
      console.error(`❌ Error extracting ${contractName}: ${error.message}`);
      errorCount++;
    }
  }

  if (errorCount > 0) {
    console.error(`\n❌ Failed to extract ${errorCount} contract(s)`);
    process.exit(1);
  }

  // Generate bytecode.go file
  console.log(`\n📝 Generating bytecode.go...`);
  const goContent = generateBytecodeGo(contracts);
  fs.writeFileSync(BYTECODE_GO_FILE, goContent, 'utf8');
  
  // Get version info for logging
  const gitVersion = getGitVersion();
  const timestamp = getCurrentTimestamp();
  
  console.log(`✓ Generated bytecode.go (${successCount} contracts)`);
  console.log(`  Git: ${gitVersion.substring(0, 8)}...`);
  console.log(`  Time: ${timestamp}`);
  console.log(`\n📁 File written to: ${BYTECODE_GO_FILE}`);
}

// Run main function
if (require.main === module) {
  main();
}

module.exports = { extractBytecode, generateBytecodeGo, CONTRACTS };

